魔法はどれか一つの判断にあるのではなく、それらが*連鎖する*ことにあります。良いポジションはハンド選びを楽にする。厳しいハンド選びはあなたのレイズを怖くする。怖いレイズはフロップでより多くのポットを取る。そして降りどきを知っていれば、負けるポットを小さく抑えられる。一つのつながりを外すと、鎖は切れます。一つずつ見ていきましょう。

